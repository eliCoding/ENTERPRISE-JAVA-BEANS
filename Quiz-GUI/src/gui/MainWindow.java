    private void btnAddTravelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTravelActionPerformed
        try {
            String fullName = tfFullName.getText();
            String destination = tfDestination.getText();
            String departureDateStr = tfDeparture.getText();
            Date departureDate = new SimpleDateFormat("yyyy-MM-dd").parse(departureDateStr);
            String returnDateStr = tfReturn.getText();
            Date returnDate = new SimpleDateFormat("yyyy-MM-dd").parse(returnDateStr);
            Travel t = new Travel(fullName, destination, departureDate, returnDate);
            travelService.addTravel(t);
            refreshTravelList();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error Adding travel:" + e.getMessage());
        }
    }//GEN-LAST:event_btnAddTravelActionPerformed

