    private void refreshTravelList() {
        try {
            Travel[] travelList = travelService.getAllTravels();
            travelListModel.clear();
            for (Travel t : travelList) {
                travelListModel.addElement(t);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Exception  occured: " + e.getMessage());

        }
    }

